function getPhrase () {
  return phrases[Math.floor(Math.random()*phrases.length)]
}

function getPhrases (length) {
  if (!length) length = 1
  var happyPhrases = []
  for (var i=0; i<length; i++) {
    happyPhrases.push(getPhrase())
  }
  return happyPhrases.join(' ')
}

